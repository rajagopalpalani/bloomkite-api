package com.sowisetech.advisor.model;

public class Dashboard {

	private int plannedUser;
	private int sharedPlan;
	private int followers;
	private int following;
	public int getPlannedUser() {
		return plannedUser;
	}
	public void setPlannedUser(int plannedUser) {
		this.plannedUser = plannedUser;
	}
	public int getSharedPlan() {
		return sharedPlan;
	}
	public void setSharedPlan(int sharedPlan) {
		this.sharedPlan = sharedPlan;
	}
	public int getFollowers() {
		return followers;
	}
	public void setFollowers(int followers) {
		this.followers = followers;
	}
	public int getFollowing() {
		return following;
	}
	public void setFollowing(int following) {
		this.following = following;
	}
}
