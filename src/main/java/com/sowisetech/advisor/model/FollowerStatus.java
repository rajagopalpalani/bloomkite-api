package com.sowisetech.advisor.model;

public class FollowerStatus {

	private long followerStatusId;
	private String status;

	public long getFollowerStatusId() {
		return followerStatusId;
	}

	public void setFollowerStatusId(long followerStatusId) {
		this.followerStatusId = followerStatusId;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

}
