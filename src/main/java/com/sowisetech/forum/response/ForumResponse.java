package com.sowisetech.forum.response;

public class ForumResponse {

	ForumResponseMessage responseMessage;
	ForumResponseData responseData;

	public ForumResponseMessage getResponseMessage() {
		return responseMessage;
	}

	public void setResponseMessage(ForumResponseMessage responseMessage) {
		this.responseMessage = responseMessage;
	}

	public ForumResponseData getResponseData() {
		return responseData;
	}

	public void setResponseData(ForumResponseData responseData) {
		this.responseData = responseData;
	}

}
